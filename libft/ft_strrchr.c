#include "libft.h"

char	*ft_strrchr(char *str, int c)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (c == str[i])
			return (&str[i]);
		i--;
	}
	return (0);
}
